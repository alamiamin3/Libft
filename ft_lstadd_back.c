#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (!*lst)
		*lst = new;
	tmp = *lst;
	tmp = ft_lstlast(tmp);
	tmp->next = new;
	new->next = NULL;
}
